package org.example.gametgweb.characterSelection.domain.model.skills;

/**
 * Интерфейс для всех боевых способностей юнита.
 * Каждая способность может проверять возможность активации
 * и возвращать новое состояние после использования.
 */
public interface UnitSkill {

    /**
     * Уникальное название способности.
     * Например: "Deflection", "PowerStrike" и т.д.
     */
    String getName();

    /**
     * Проверяет, можно ли активировать способность сейчас.
     * Например, есть ли заряды, не в перезарядке и т.д.
     */
    boolean canActivate();

    /**
     * Активация способности.
     * Возвращает новый объект способности с обновлённым состоянием (immutable).
     */
    UnitSkill activate();
}
